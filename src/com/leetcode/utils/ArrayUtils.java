public class ArrayUtils {

  public static void printArray(String[] arr) {
    int size = arr.length;
    for (int i = 0; i < size; i++) {
      System.out.println(arr[i]);
    }
  }

  public static void printArray(Integer[] arr) {
    int size = arr.length;
    for (int i = 0; i < size; i++) {
      System.out.println(arr[i]);
    }
  }
}